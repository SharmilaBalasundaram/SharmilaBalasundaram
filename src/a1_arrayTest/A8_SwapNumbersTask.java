package a1_arrayTest;

public class A8_SwapNumbersTask {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		
		System.out.print("Before swapping of a and b:"+a+","+b);
		
		System.out.println();
		
		int temp=a;
		a=b;
		b=temp;
		
		System.out.print("After swapping of a and b:"+a+","+b);
		
	}
	
}
